// Drag and drop is a user interaction technique that allows users to click and drag an element and drop it onto a target area.

// Events used for drag and drop
// 1. dragstart — when dragging starts
// 2. dragover — when dragging over a valid drop target
// 3. drop — when the dragged item is dropped
// 4. dragenter and dragleave — when the dragged item enters or leaves a drop target
// 5. dragend — when dragging ends